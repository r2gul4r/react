package ch02_데이터와변수;

public class Ex05_char정수 {

	public static void main(String[] args) {
		
		//문자 1자는 단일 따옴표 1자 이상은 이중따옴
		
		//문자 저장
		char var1 = 'A';
		char var5 = '가';
		char var7 = '가';
		
		//문자열 저장
		String var2 = "A";
		String var8 = "";
		
		//유니코드 직접 저장
		
		char var3 = 65;
		char var4 = 44032;
		
		
		System.out.println("var1: " + var1);
		System.out.println("var2: " + var2);
		System.out.println("var3: " + var3);
		System.out.println("var4: " + var4);
	}

}
